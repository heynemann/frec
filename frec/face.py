class Face():
    def __init__(self, x, y, width, height):
        self.x = x
        self.y = y
        self.width = width
        self.height = height

    def __repr__(self):
        return "Face found at %dx%d(x, y) measuring %dx%d" % (self.x, self.y, self.width, self.height)
